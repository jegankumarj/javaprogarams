package com.loop;

import java.util.Scanner;

public class NaturalNum {

	public static void main(String[] args) {
		int i,s=0,num;
		i=1;
		Scanner sc= new Scanner(System.in);
		System.out.println("Enter a  Natural num  ");
		num=sc.nextInt();
		while(i<=num)  {
			s=s+i;
			i=i+1;
		}
       System.out.println("total num of natural nums "+s);
       
       
       
       
       
       
       
       
       
       
       
       
       
	}
}
